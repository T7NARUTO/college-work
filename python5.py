# Add,Sub,Mul, and Div of Hexadecimal Numbers

a = int(input("Enter a Hexadecimal number"), 16)
b = int(input("Enter a Hexadecimal number"), 16)
add = a + b
sub = a - b
mul = a * b
div = int(a / b)
print("Addition=", hex(add), "Subtraction=", hex(sub), "Multiplication=", hex(mul), "Division=", hex(div))
